<?php

declare(strict_types=1);

use App\Models\Profile;

return [

    /*
    |--------------------------------------------------------------------------
    | Authentication Defaults
    |--------------------------------------------------------------------------
    |
    | 既定ガードはセッションベースの "web" ガードで、プロバイダは `profiles`。
    | パスワード再設定は Supabase に委譲するため、Laravel のパスワードブローカは
    | 設定しない。
    |
    */

    'defaults' => [
        'guard' => env('AUTH_GUARD', 'web'),
    ],

    /*
    |--------------------------------------------------------------------------
    | Authentication Guards
    |--------------------------------------------------------------------------
    */

    'guards' => [
        'web' => [
            'driver' => 'session',
            'provider' => 'profiles',
        ],
    ],

    /*
    |--------------------------------------------------------------------------
    | User Providers
    |--------------------------------------------------------------------------
    |
    | 認証対象は `profiles` レコードで、その id は Supabase auth.users の UUID
    | （JWT の `sub`）と同一。ローカルに users テーブルは持たない。
    |
    */

    'providers' => [
        'profiles' => [
            'driver' => 'eloquent',
            'model' => env('AUTH_MODEL', Profile::class),
        ],
    ],

    /*
    |--------------------------------------------------------------------------
    | Password Confirmation Timeout
    |--------------------------------------------------------------------------
    */

    'password_timeout' => env('AUTH_PASSWORD_TIMEOUT', 10800),

];
