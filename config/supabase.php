<?php

declare(strict_types=1);

return [

    /*
    |--------------------------------------------------------------------------
    | Supabase プロジェクト URL（サーバ専用）
    |--------------------------------------------------------------------------
    */
    'url' => env('SUPABASE_URL'),

    /*
    |--------------------------------------------------------------------------
    | アクセストークンの署名検証（非対称）に用いる JWKS エンドポイント
    |--------------------------------------------------------------------------
    */
    'jwks_url' => env('SUPABASE_JWKS_URL'),

    /*
    |--------------------------------------------------------------------------
    | 期待する JWT の issuer。未設定時は <SUPABASE_URL>/auth/v1 を既定とする。
    |--------------------------------------------------------------------------
    */
    'issuer' => env('SUPABASE_JWT_ISSUER') ?: (env('SUPABASE_URL') ? rtrim(env('SUPABASE_URL'), '/').'/auth/v1' : null),

    /*
    |--------------------------------------------------------------------------
    | 期待する audience クレーム。
    |--------------------------------------------------------------------------
    */
    'audience' => env('SUPABASE_JWT_AUD', 'authenticated'),

    /*
    |--------------------------------------------------------------------------
    | JWKS のキャッシュ TTL（秒）。鍵はキャッシュし、リクエスト毎には取得しない。
    |--------------------------------------------------------------------------
    */
    'jwks_cache_ttl' => (int) env('SUPABASE_JWKS_CACHE_TTL', 3600),
];
