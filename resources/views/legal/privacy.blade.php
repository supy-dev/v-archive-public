        <h3>1.3 YouTube API Servicesにより取得する公開情報</h3>
        <p>本サービスは、YouTube API Servicesを利用して、YouTube上で公開されているチャンネル情報および動画情報を取得・表示・保存する場合があります。</p>
        <ul>
            <li>YouTubeチャンネル名</li>
            <li>チャンネルID</li>
            <li>チャンネル説明文</li>
